package com.kjh.ja01;

public class Jamong {
	private String name;
	private int age;
	
	public Jamong() {
		super();
	}

	public Jamong(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
}
